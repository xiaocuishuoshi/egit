package com.whfp.oa.manager.jd.bean;

import org.hibernate.type.Type;

public class SQLQueryMapping {
	public String name;
	public Type type;

	public SQLQueryMapping(String name, Type type) {
		this.name = name;
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Type getType() {
		return type;
	}

	public void setType(Type type) {
		this.type = type;
	}
}
