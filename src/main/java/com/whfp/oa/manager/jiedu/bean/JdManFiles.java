package com.whfp.oa.manager.jiedu.bean;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "jd_man_files")
public class JdManFiles {

	@Id
	@GeneratedValue(strategy=GenerationType.AUTO)
	private Long id;
	
	@Column(name="user_id")
	private String userId;
	
	@Column(name="act_type")
	private Integer act_type;
	
	@Column(name="file_name")
	private String file_name;
	
	@Column(name="file_sec")
	private String file_sec;
	
	@Column(name="file_ext")
	private String file_ext;
	
	@Column(name="file_path")
	private String file_path;
	
	@Column(name="file_url")
	private String file_url;
	
	@Column(name="is_private")
	private Integer is_private;
	
	@Column(name="check_token")
	private String check_token;
	
	@Column(name="create_uid")
	private String createUid;
	
	@Column(name="create_date")
	private Date createDate;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Integer getAct_type() {
		return act_type;
	}

	public void setAct_type(Integer act_type) {
		this.act_type = act_type;
	}

	public String getFile_name() {
		return file_name;
	}

	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}

	public String getFile_sec() {
		return file_sec;
	}

	public void setFile_sec(String file_sec) {
		this.file_sec = file_sec;
	}

	public String getFile_ext() {
		return file_ext;
	}

	public void setFile_ext(String file_ext) {
		this.file_ext = file_ext;
	}

	public String getFile_path() {
		return file_path;
	}

	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}

	public String getFile_url() {
		return file_url;
	}

	public void setFile_url(String file_url) {
		this.file_url = file_url;
	}

	public Integer getIs_private() {
		return is_private;
	}

	public void setIs_private(Integer is_private) {
		this.is_private = is_private;
	}

	public String getCheck_token() {
		return check_token;
	}

	public void setCheck_token(String check_token) {
		this.check_token = check_token;
	}

	public String getCreateUid() {
		return createUid;
	}

	public void setCreateUid(String createUid) {
		this.createUid = createUid;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	
}
