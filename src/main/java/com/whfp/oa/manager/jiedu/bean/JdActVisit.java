package com.whfp.oa.manager.jiedu.bean;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "jd_act_visit")
public class JdActVisit {

	@Id
	@GeneratedValue(strategy=GenerationType.AUTO)
	private Long id;
	
	@Column(name="to_uid")
	private String toUid;
	
	@Column(name="to_user")
	private String toUser;
	
	@Column(name="to_visitor")
	private String toVisitor;
	
	@Column(name="to_visitor_relationship")
	private String toVisitorRelationship;
	
	@Column(name="to_visitor_phone")
	private String toVisitorPhone;
	
	@Column(name="reason_type")
	private String reasonType;
	
	private String address;
	
	@Column(name="visitor_uid")
	private String visitorUid;
	
	@Column(name="visitor_name")
	private String visitorName;
	
	@Column(name="visitor_date")
	private String visitorDate;
	
	private String content;
	
	private String key1;
	
	private String key2;
	
	private String key3;
	
	private String key4;
	
	private String key5;
	
	@Column(name="create_uid")
	private String createUid;
	
	@Column(name="create_date")
	private Date createDate;

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getToUid() {
		return toUid;
	}

	public void setToUid(String toUid) {
		this.toUid = toUid;
	}

	public String getToUser() {
		return toUser;
	}

	public void setToUser(String toUser) {
		this.toUser = toUser;
	}

	public String getToVisitor() {
		return toVisitor;
	}

	public void setToVisitor(String toVisitor) {
		this.toVisitor = toVisitor;
	}

	public String getToVisitorRelationship() {
		return toVisitorRelationship;
	}

	public void setToVisitorRelationship(String toVisitorRelationship) {
		this.toVisitorRelationship = toVisitorRelationship;
	}

	public String getToVisitorPhone() {
		return toVisitorPhone;
	}

	public void setToVisitorPhone(String toVisitorPhone) {
		this.toVisitorPhone = toVisitorPhone;
	}

	public String getVisitorUid() {
		return visitorUid;
	}

	public void setVisitorUid(String visitorUid) {
		this.visitorUid = visitorUid;
	}

	public String getVisitorName() {
		return visitorName;
	}

	public void setVisitorName(String visitorName) {
		this.visitorName = visitorName;
	}

	public String getVisitorDate() {
		return visitorDate;
	}

	public void setVisitorDate(String visitorDate) {
		this.visitorDate = visitorDate;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getKey1() {
		return key1;
	}

	public void setKey1(String key1) {
		this.key1 = key1;
	}

	public String getKey2() {
		return key2;
	}

	public void setKey2(String key2) {
		this.key2 = key2;
	}

	public String getKey3() {
		return key3;
	}

	public void setKey3(String key3) {
		this.key3 = key3;
	}

	public String getKey4() {
		return key4;
	}

	public void setKey4(String key4) {
		this.key4 = key4;
	}

	public String getKey5() {
		return key5;
	}

	public void setKey5(String key5) {
		this.key5 = key5;
	}

	public String getCreateUid() {
		return createUid;
	}

	public void setCreateUid(String createUid) {
		this.createUid = createUid;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getReasonType() {
		return reasonType;
	}

	public void setReasonType(String reasonType) {
		this.reasonType = reasonType;
	}
	
}
